package AnudipPract;
public class CompareNumbers {
	public static void main(String[] args) {

		int numerator = 10;
		int denominator = 20;

		// Compare the values
		if (numerator > denominator) {
			System.out.println("Greater");
		} else if (numerator == denominator) {
			System.out.println("Equal");
		} else {
			System.out.println("Less");
		}
	}
}
